export const getNFTs = async (nftAddress, tokenId) => {
  try {
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const contract = new ethers.Contract(
      MARKETPLACE_ADDRESS,
      MARKETPLACE_ABI,
      provider
    );

    // ðŸ”¹ Call the smart contract function
    const listing = await contract.getListings(nftAddress, tokenId);

    // ðŸ”¹ Structure the response
    return {
      seller: listing[0],
      price: ethers.utils.formatEther(listing[1]), // Convert Wei to ETH
      isActive: listing[2],
    };
  } catch (error) {
    console.error("Error fetching NFTs:", error);
    return null;
  }
};

export const getAllNFTs = async () => {
  try {
    const provider = new ethers.BrowserProvider(window.ethereum);
    const contract = new ethers.Contract(
      NFT_CONTRACT_ADDRESS,
      NFT_ABI,
      provider
    );

    const totalSupply = await contract.totalSupply();
    let nftList = [];

    for (let i = 0; i < totalSupply; i++) {
      const tokenId = await contract.tokenByIndex(i);
      const tokenURI = await contract.tokenURI(tokenId);

      // Fetch metadata from tokenURI (assuming it's a JSON file)
      const metadata = await fetch(tokenURI).then((res) => res.json());

      nftList.push({
        tokenId: tokenId.toString(),
        name: metadata.name,
        image: metadata.image,
        price: metadata.price || "N/A", // Price not stored in NFT contract
      });
    }

    return nftList;
  } catch (error) {
    console.error("Error fetching NFTs:", error);
    return [];
  }
};
